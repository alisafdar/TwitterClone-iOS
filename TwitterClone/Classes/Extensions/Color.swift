import SwiftUI

extension Color {
	public static func purpleColor() -> Color{
		return Color.init("TitleColor")
	}
	
	public static func greyColor() -> Color{
		return Color.init("TextFieldBorderColor")
	}
	
	public static func buttonStartColor() -> Color{
		return Color.init("ButtonColorStart")
	}
	
	public static func buttonEndColor() -> Color{
		return Color.init("ButtonColorEnd")
	}
	
	public static func whiteBackgroundColor() -> Color{
		return Color.init("WhiteBackground")
	}
}
